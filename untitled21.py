from transformers import pipeline

classifier = pipeline("sentiment-analysis")

# Step 4: Display Message
print("Sentiment Analysis using Hugging Face Transfer Learning")
print("Type 'exit' to stop the program")

# Step 5: Loop for Multiple Inputs
while True:

    # Step 6: Get Input from User
    text = input("\nEnter a sentence: ")

    # Step 7: Stop Condition
    if text.lower() == "exit":
        print("Program stopped")
        break

    # Step 8: Perform Sentiment Prediction
    result = classifier(text)

    # Step 9: Extract Result
    label = result[0]['label']
    score = result[0]['score']

    # Step 10: Display Output
    print("Sentence :", text)
    print("Sentiment :", label)
    print("Confidence Score :", score)

